def RK4(f, x0, t0, tf, N):
    t = np.linspace(t0, tf, N)
    dt = t[1] - t[0]
    x = np.zeros((len(x0), N))
    x[:, 0] = x0
    for i in range(1, N):
        k1 = f(t[i-1], x[:, i-1])
        k2 = f(t[i-1] + dt/2, x[:, i-1] + dt/2 * k1)
        k3 = f(t[i-1] + dt/2, x[:, i-1] + dt/2 * k2)
        k4 = f(t[i-1] + dt, x[:, i-1] + dt * k3)
        x[:, i] = x[:, i-1] + dt/6 * (k1 + 2*k2 + 2*k3 + k4)
    return t, x

